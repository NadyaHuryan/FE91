function getSum(a, b) {
  if (a === b) return a;
  let min = a;
  let max = b;
  let sum_i = 0;
  if (a > b) {
    min = b;
    max = a;
  }
  for (let i = min; i <= max; i++) {
    sum_i += i;
  }
  return sum_i;
}

